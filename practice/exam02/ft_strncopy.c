#include <stdio.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (src[i] && i < n)
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
	}
	return (dest);
}

int	main(int argc, char **argv)
{
	if (argc == 3)
		printf("%s", ft_strncpy(argv[1], argv[2], 3));
}
